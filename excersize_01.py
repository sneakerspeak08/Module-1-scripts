

gradentry = input("Enter a grade 0-100: ")

if int(gradentry) >= 90:
    print("Your Grade is A") 
elif int(gradentry) >= 80:
    print("Your Grade is B") 
elif int(gradentry) >= 70:
    print("Your Grade is C") 
elif int(gradentry) >= 60:
    print("Your Grade is D") 
else:
    print("Your Grade is F") 

